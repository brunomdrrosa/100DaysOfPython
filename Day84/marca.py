from PIL import Image
import matplotlib.pyplot as plt
import numpy as np

"""# Abrir imagem"""

image = Image.open("top.jpg")
image.show()
plt.imshow(image)

size = (500, 100)
crop_image = image.copy()
crop_image.thumbnail(size)

"""# Imagem que ser치 a marca d'치gua"""

marca = Image.open("logo.png")
marca.show()
plt.imshow(marca)

"""# Marca d'치gua inserida na imagem escolhida"""

copied_image = image.copy()
copied_image.paste(marca, (1780, 1050))
plt.imshow(copied_image)